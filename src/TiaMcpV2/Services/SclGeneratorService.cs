            sb.AppendLine("                _phase := _PHASE_RUNNING;");
            sb.AppendLine("                _step := 1;");
            sb.AppendLine("                _stepTimer := 0;");
            sb.AppendLine("            END_IF;");
            sb.AppendLine();
            sb.AppendLine("        _PHASE_RUNNING:");
            sb.AppendLine("            _stepTimer := _stepTimer + 1;");
