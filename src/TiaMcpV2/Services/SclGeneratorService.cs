            sb.AppendLine("                    _stepTimer := 0;");
            sb.AppendLine("                END_IF;");
            sb.AppendLine("            END_IF;");
            sb.AppendLine("            IF _step > _totalSteps THEN");
            sb.AppendLine("                _phase := _PHASE_COMPLETE;");
            sb.AppendLine("            ELSIF iCmdHold THEN");
            sb.AppendLine("                _phase := _PHASE_HELD;");
            sb.AppendLine("            ELSIF iCmdAbort OR NOT iEnable THEN");
            sb.AppendLine("                _phase := _PHASE_ABORTED;");
            sb.AppendLine("            END_IF;");
            sb.AppendLine();
            sb.AppendLine("        _PHASE_HELD:");
            sb.AppendLine("            IF iCmdResume THEN");
            sb.AppendLine("                _phase := _PHASE_RUNNING;");
            sb.AppendLine("            ELSIF iCmdAbort THEN");
            sb.AppendLine("                _phase := _PHASE_ABORTED;");
            sb.AppendLine("            END_IF;");
            sb.AppendLine();
            sb.AppendLine("        _PHASE_COMPLETE, _PHASE_ABORTED:");
            sb.AppendLine("            IF iCmdReset AND NOT _ResetPrev THEN");
            sb.AppendLine("                _phase := _PHASE_IDLE;");
            sb.AppendLine("                _step := 0;");
            sb.AppendLine("            END_IF;");
            sb.AppendLine("    END_CASE;");
            sb.AppendLine("END_REGION");
            sb.AppendLine();
            sb.AppendLine("REGION Output mapping");
            sb.AppendLine("    _StartPrev := iCmdStart;");
            sb.AppendLine("    _ResetPrev := iCmdReset;");
            sb.AppendLine("    oPhase := _phase;");
            sb.AppendLine("    oCurrentStep := _step;");
            sb.AppendLine("    oIsRunning := (_phase = _PHASE_RUNNING);");
            sb.AppendLine("    oIsHeld := (_phase = _PHASE_HELD);");
            sb.AppendLine("    oIsComplete := (_phase = _PHASE_COMPLETE);");
            sb.AppendLine("    oIsAborted := (_phase = _PHASE_ABORTED);");
            sb.AppendLine("    oStepTime := DINT_TO_TIME(_stepTimer * 10); // 10ms cycle assumed");
            sb.AppendLine("    // Set step active flags");
            sb.AppendLine("    FOR _t_i := 1 TO 20 DO");
            sb.AppendLine("        oStepActive[DINT_TO_INT(_t_i)] := (_step = _t_i) AND (_phase = _PHASE_RUNNING);");
            sb.AppendLine("    END_FOR;");
            sb.AppendLine("END_REGION");
            sb.AppendLine();
            sb.AppendLine("END_FUNCTION_BLOCK");
            return sb.ToString();
        }

