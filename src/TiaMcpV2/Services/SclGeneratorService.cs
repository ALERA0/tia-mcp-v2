        private string GenerateMotorDOL(string blockName, Dictionary<string, string>? p)
        {
            var sb = new StringBuilder();
            sb.AppendLine($"FUNCTION_BLOCK \"{blockName}\"");
            sb.AppendLine($"TITLE = 'Direct On-Line Motor Control'");
            sb.AppendLine("{ S7_Optimized_Access := 'TRUE' }");
            sb.AppendLine("AUTHOR : 'TiaMcpV2'");
            sb.AppendLine("FAMILY : 'Drives'");
            sb.AppendLine("VERSION : 1.0");
            sb.AppendLine("//Direct On-Line motor control with feedback monitoring, runtime counter and fault handling");
            sb.AppendLine();
            sb.AppendLine("   VAR_INPUT");
            sb.AppendLine("      iCmdStart : Bool;          // Start command");
            sb.AppendLine("      iCmdStop : Bool;            // Stop command");
            sb.AppendLine("      iCmdReset : Bool;           // Fault reset command");
            sb.AppendLine("      iFeedbackRunning : Bool;    // Motor running feedback (from contactor aux)");
            sb.AppendLine("      iThermalTrip : Bool;        // Thermal overload relay (NC contact, FALSE=tripped)");
            sb.AppendLine("      iEnable : Bool := TRUE;     // Enable signal (safety interlock)");
            sb.AppendLine("      iAutoMode : Bool;           // TRUE=Auto mode, FALSE=Manual mode");
            sb.AppendLine("      iAutoStart : Bool;          // Auto mode start command");
            sb.AppendLine("      iAutoStop : Bool;           // Auto mode stop command");
            sb.AppendLine("      iCfg_FeedbackTime : Time := T#5S; // Max time for feedback after start");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("   VAR_OUTPUT");
            sb.AppendLine("      oContactor : Bool;          // Contactor output");
            sb.AppendLine("      oIsRunning : Bool;          // Motor running status");
            sb.AppendLine("      oFault : Bool;              // Fault active");
            sb.AppendLine("      oFaultCode : Int;           // Fault code (0=None, 1=Thermal, 2=FeedbackTimeout, 3=FeedbackLost)");
            sb.AppendLine("      oWarning : Bool;            // Warning active");
            sb.AppendLine("      oReadyToStart : Bool;       // Motor ready to start");
            sb.AppendLine("      oRuntimeHours : DInt;       // Total runtime in hours");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("   VAR");
            sb.AppendLine("      _state : DInt;              // Internal state (0=Stop, 1=Starting, 2=Running, 3=Stopping, 10=Fault)");
            sb.AppendLine("      _StartCmdPrev : Bool;       // Previous start command for edge detection");
            sb.AppendLine("      _ResetCmdPrev : Bool;       // Previous reset command for edge detection");
            sb.AppendLine("      _FeedbackTimer : Int;       // Feedback timeout counter (cycles)");
            sb.AppendLine("      _RuntimeCounter : DInt;     // Runtime counter in seconds");
            sb.AppendLine("      _CycleCounter : DInt;       // Sub-second cycle counter");
            sb.AppendLine("      _StartCount : DInt;         // Total start count");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("   VAR_TEMP");
            sb.AppendLine("      _t_start_edge : Bool;");
            sb.AppendLine("      _t_reset_edge : Bool;");
            sb.AppendLine("      _t_cmd_start : Bool;");
            sb.AppendLine("      _t_cmd_stop : Bool;");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("   VAR CONSTANT");
            sb.AppendLine("      _STATE_STOPPED : DInt := 0;");
            sb.AppendLine("      _STATE_STARTING : DInt := 1;");
            sb.AppendLine("      _STATE_RUNNING : DInt := 2;");
            sb.AppendLine("      _STATE_STOPPING : DInt := 3;");
            sb.AppendLine("      _STATE_FAULT : DInt := 10;");
            sb.AppendLine("      _FAULT_NONE : Int := 0;");
            sb.AppendLine("      _FAULT_THERMAL : Int := 1;");
            sb.AppendLine("      _FAULT_FEEDBACK_TIMEOUT : Int := 2;");
            sb.AppendLine("      _FAULT_FEEDBACK_LOST : Int := 3;");
            sb.AppendLine("      _FEEDBACK_TIMEOUT_CYCLES : Int := 500; // ~5s at 10ms cycle");
            sb.AppendLine("      _CYCLES_PER_SECOND : DInt := 100;      // Adjust to actual OB cycle");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("BEGIN");
            sb.AppendLine();
            sb.AppendLine("REGION Edge detection");
            sb.AppendLine("    _t_start_edge := iCmdStart AND NOT _StartCmdPrev;");
            sb.AppendLine("    _t_reset_edge := iCmdReset AND NOT _ResetCmdPrev;");
            sb.AppendLine("    _StartCmdPrev := iCmdStart;");
            sb.AppendLine("    _ResetCmdPrev := iCmdReset;");
            sb.AppendLine("END_REGION");
            sb.AppendLine();
            sb.AppendLine("REGION Auto/Manual command selection");
            sb.AppendLine("    IF iAutoMode THEN");
            sb.AppendLine("        _t_cmd_start := iAutoStart;");
            sb.AppendLine("        _t_cmd_stop := iAutoStop;");
            sb.AppendLine("    ELSE");
            sb.AppendLine("        _t_cmd_start := _t_start_edge;");
            sb.AppendLine("        _t_cmd_stop := iCmdStop;");
            sb.AppendLine("    END_IF;");
            sb.AppendLine("END_REGION");
            sb.AppendLine();
            sb.AppendLine("REGION Thermal trip monitoring");
            sb.AppendLine("    IF NOT iThermalTrip THEN");
            sb.AppendLine("        _state := _STATE_FAULT;");
            sb.AppendLine("        oFaultCode := _FAULT_THERMAL;");
            sb.AppendLine("    END_IF;");
            sb.AppendLine("END_REGION");
            sb.AppendLine();
            sb.AppendLine("REGION State machine");
            sb.AppendLine("    CASE _state OF");
            sb.AppendLine("        _STATE_STOPPED:");
            sb.AppendLine("            oContactor := FALSE;");
            sb.AppendLine("            IF _t_cmd_start AND iEnable AND iThermalTrip THEN");
            sb.AppendLine("                _state := _STATE_STARTING;");
            sb.AppendLine("                _FeedbackTimer := 0;");
            sb.AppendLine("                _StartCount := _StartCount + 1;");
            sb.AppendLine("            END_IF;");
            sb.AppendLine();
            sb.AppendLine("        _STATE_STARTING:");
            sb.AppendLine("            oContactor := TRUE;");
            sb.AppendLine("            _FeedbackTimer := _FeedbackTimer + 1;");
            sb.AppendLine("            IF iFeedbackRunning THEN");
            sb.AppendLine("                _state := _STATE_RUNNING;");
            sb.AppendLine("            ELSIF _FeedbackTimer >= _FEEDBACK_TIMEOUT_CYCLES THEN");
            sb.AppendLine("                _state := _STATE_FAULT;");
            sb.AppendLine("                oFaultCode := _FAULT_FEEDBACK_TIMEOUT;");
            sb.AppendLine("            ELSIF _t_cmd_stop OR NOT iEnable THEN");
            sb.AppendLine("                _state := _STATE_STOPPING;");
            sb.AppendLine("            END_IF;");
            sb.AppendLine();
            sb.AppendLine("        _STATE_RUNNING:");
            sb.AppendLine("            oContactor := TRUE;");
            sb.AppendLine("            IF NOT iFeedbackRunning THEN");
            sb.AppendLine("                _state := _STATE_FAULT;");
            sb.AppendLine("                oFaultCode := _FAULT_FEEDBACK_LOST;");
            sb.AppendLine("            ELSIF _t_cmd_stop OR NOT iEnable THEN");
            sb.AppendLine("                _state := _STATE_STOPPING;");
            sb.AppendLine("            END_IF;");
            sb.AppendLine();
            sb.AppendLine("        _STATE_STOPPING:");
            sb.AppendLine("            oContactor := FALSE;");
            sb.AppendLine("            IF NOT iFeedbackRunning THEN");
            sb.AppendLine("                _state := _STATE_STOPPED;");
            sb.AppendLine("            END_IF;");
            sb.AppendLine();
            sb.AppendLine("        _STATE_FAULT:");
            sb.AppendLine("            oContactor := FALSE;");
            sb.AppendLine("            IF _t_reset_edge AND iThermalTrip THEN");
            sb.AppendLine("                _state := _STATE_STOPPED;");
            sb.AppendLine("                oFaultCode := _FAULT_NONE;");
            sb.AppendLine("            END_IF;");
            sb.AppendLine("    END_CASE;");
            sb.AppendLine("END_REGION");
            sb.AppendLine();
            sb.AppendLine("REGION Output mapping");
            sb.AppendLine("    oIsRunning := (_state = _STATE_RUNNING);");
            sb.AppendLine("    oFault := (_state = _STATE_FAULT);");
            sb.AppendLine("    oWarning := NOT iThermalTrip AND (_state <> _STATE_FAULT);");
            sb.AppendLine("    oReadyToStart := (_state = _STATE_STOPPED) AND iEnable AND iThermalTrip;");
            sb.AppendLine("END_REGION");
            sb.AppendLine();
            sb.AppendLine("REGION Runtime counter");
            sb.AppendLine("    IF oIsRunning THEN");
            sb.AppendLine("        _CycleCounter := _CycleCounter + 1;");
            sb.AppendLine("        IF _CycleCounter >= _CYCLES_PER_SECOND THEN");
            sb.AppendLine("            _CycleCounter := 0;");
            sb.AppendLine("            _RuntimeCounter := _RuntimeCounter + 1;");
            sb.AppendLine("        END_IF;");
            sb.AppendLine("    END_IF;");
            sb.AppendLine("    oRuntimeHours := _RuntimeCounter / 3600;");
            sb.AppendLine("END_REGION");
            sb.AppendLine();
            sb.AppendLine("END_FUNCTION_BLOCK");

            return sb.ToString();
        }

        private string GenerateMotorStarDelta(string blockName, Dictionary<string, string>? p)
        {
            var transitionTime = p?.ContainsKey("TransitionTime") == true ? p["TransitionTime"] : "T#6S";
            var sb = new StringBuilder();
            sb.AppendLine($"FUNCTION_BLOCK \"{blockName}\"");
            sb.AppendLine("TITLE = 'Star-Delta Motor Control'");
            sb.AppendLine("{ S7_Optimized_Access := 'TRUE' }");
            sb.AppendLine("AUTHOR : 'TiaMcpV2'");
            sb.AppendLine("FAMILY : 'Drives'");
            sb.AppendLine("VERSION : 1.0");
            sb.AppendLine("//Star-Delta motor control with Y/D transition, thermal protection and fault handling");
            sb.AppendLine();
            sb.AppendLine("   VAR_INPUT");
            sb.AppendLine("      iCmdStart : Bool;");
            sb.AppendLine("      iCmdStop : Bool;");
            sb.AppendLine("      iCmdReset : Bool;");
            sb.AppendLine("      iFeedbackRunning : Bool;");
            sb.AppendLine("      iThermalTrip : Bool;");
            sb.AppendLine("      iEnable : Bool := TRUE;");
            sb.AppendLine($"      iCfg_TransitionTime : Time := {transitionTime};");
            sb.AppendLine("      iCfg_FeedbackTime : Time := T#5S;");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("   VAR_OUTPUT");
            sb.AppendLine("      oMainContactor : Bool;      // K1 - Main contactor");
            sb.AppendLine("      oStarContactor : Bool;       // K2 - Star contactor");
            sb.AppendLine("      oDeltaContactor : Bool;      // K3 - Delta contactor");
            sb.AppendLine("      oIsRunning : Bool;");
            sb.AppendLine("      oFault : Bool;");
            sb.AppendLine("      oFaultCode : Int;");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("   VAR");
            sb.AppendLine("      _state : DInt;              // 0=Stop, 1=StarStart, 2=StarRun, 3=Transition, 4=DeltaRun, 5=Stopping, 10=Fault");
            sb.AppendLine("      _timer : DInt;");
            sb.AppendLine("      _StartCmdPrev : Bool;");
            sb.AppendLine("      _ResetCmdPrev : Bool;");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("   VAR CONSTANT");
            sb.AppendLine("      _STATE_STOPPED : DInt := 0;");
            sb.AppendLine("      _STATE_STAR_START : DInt := 1;");
            sb.AppendLine("      _STATE_STAR_RUN : DInt := 2;");
            sb.AppendLine("      _STATE_TRANSITION : DInt := 3;");
            sb.AppendLine("      _STATE_DELTA_RUN : DInt := 4;");
            sb.AppendLine("      _STATE_STOPPING : DInt := 5;");
            sb.AppendLine("      _STATE_FAULT : DInt := 10;");
            sb.AppendLine("      _TRANSITION_CYCLES : DInt := 5;   // ~50ms gap between star-off and delta-on");
            sb.AppendLine("      _STAR_RUN_CYCLES : DInt := 600;   // ~6s star run time at 10ms cycle");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("BEGIN");
            sb.AppendLine();
            sb.AppendLine("REGION Thermal trip check");
            sb.AppendLine("    IF NOT iThermalTrip THEN");
            sb.AppendLine("        _state := _STATE_FAULT;");
            sb.AppendLine("        oFaultCode := 1;");
            sb.AppendLine("    END_IF;");
            sb.AppendLine("END_REGION");
            sb.AppendLine();
            sb.AppendLine("REGION State machine");
            sb.AppendLine("    CASE _state OF");
            sb.AppendLine("        _STATE_STOPPED:");
            sb.AppendLine("            oMainContactor := FALSE; oStarContactor := FALSE; oDeltaContactor := FALSE;");
            sb.AppendLine("            IF iCmdStart AND NOT _StartCmdPrev AND iEnable AND iThermalTrip THEN");
            sb.AppendLine("                _state := _STATE_STAR_START;");
            sb.AppendLine("                _timer := 0;");
            sb.AppendLine("            END_IF;");
            sb.AppendLine();
            sb.AppendLine("        _STATE_STAR_START:");
            sb.AppendLine("            oMainContactor := TRUE; oStarContactor := TRUE; oDeltaContactor := FALSE;");
            sb.AppendLine("            _timer := _timer + 1;");
            sb.AppendLine("            IF _timer >= _STAR_RUN_CYCLES THEN");
            sb.AppendLine("                _state := _STATE_TRANSITION;");
            sb.AppendLine("                _timer := 0;");
            sb.AppendLine("            ELSIF iCmdStop OR NOT iEnable THEN");
            sb.AppendLine("                _state := _STATE_STOPPING;");
            sb.AppendLine("            END_IF;");
            sb.AppendLine();
            sb.AppendLine("        _STATE_TRANSITION:");
            sb.AppendLine("            oMainContactor := FALSE; oStarContactor := FALSE; oDeltaContactor := FALSE;");
            sb.AppendLine("            _timer := _timer + 1;");
            sb.AppendLine("            IF _timer >= _TRANSITION_CYCLES THEN");
            sb.AppendLine("                _state := _STATE_DELTA_RUN;");
            sb.AppendLine("            END_IF;");
            sb.AppendLine();
            sb.AppendLine("        _STATE_DELTA_RUN:");
            sb.AppendLine("            oMainContactor := TRUE; oStarContactor := FALSE; oDeltaContactor := TRUE;");
            sb.AppendLine("            IF iCmdStop OR NOT iEnable THEN");
            sb.AppendLine("                _state := _STATE_STOPPING;");
            sb.AppendLine("            END_IF;");
            sb.AppendLine();
            sb.AppendLine("        _STATE_STOPPING:");
            sb.AppendLine("            oMainContactor := FALSE; oStarContactor := FALSE; oDeltaContactor := FALSE;");
            sb.AppendLine("            _state := _STATE_STOPPED;");
            sb.AppendLine();
            sb.AppendLine("        _STATE_FAULT:");
            sb.AppendLine("            oMainContactor := FALSE; oStarContactor := FALSE; oDeltaContactor := FALSE;");
            sb.AppendLine("            IF iCmdReset AND NOT _ResetCmdPrev AND iThermalTrip THEN");
            sb.AppendLine("                _state := _STATE_STOPPED;");
            sb.AppendLine("                oFaultCode := 0;");
            sb.AppendLine("            END_IF;");
            sb.AppendLine("    END_CASE;");
            sb.AppendLine("END_REGION");
            sb.AppendLine();
            sb.AppendLine("REGION Outputs");
            sb.AppendLine("    _StartCmdPrev := iCmdStart;");
            sb.AppendLine("    _ResetCmdPrev := iCmdReset;");
            sb.AppendLine("    oIsRunning := (_state = _STATE_STAR_START) OR (_state = _STATE_DELTA_RUN);");
            sb.AppendLine("    oFault := (_state = _STATE_FAULT);");
            sb.AppendLine("END_REGION");
            sb.AppendLine();
            sb.AppendLine("END_FUNCTION_BLOCK");
            return sb.ToString();
        }

        private string GenerateMotorVFD(string blockName, Dictionary<string, string>? p)
        {
            var sb = new StringBuilder();
            sb.AppendLine($"FUNCTION_BLOCK \"{blockName}\"");
            sb.AppendLine("TITLE = 'VFD Motor Control'");
            sb.AppendLine("{ S7_Optimized_Access := 'TRUE' }");
            sb.AppendLine("AUTHOR : 'TiaMcpV2'");
            sb.AppendLine("FAMILY : 'Drives'");
            sb.AppendLine("VERSION : 1.0");
            sb.AppendLine("//Variable Frequency Drive motor control for SINAMICS drives via PROFINET/PROFIBUS");
            sb.AppendLine();
            sb.AppendLine("   VAR_INPUT");
            sb.AppendLine("      iCmdStart : Bool;");
            sb.AppendLine("      iCmdStop : Bool;");
            sb.AppendLine("      iCmdReset : Bool;");
            sb.AppendLine("      iEnable : Bool := TRUE;");
            sb.AppendLine("      iSpeedSetpoint : Real;      // Speed setpoint in % (0.0 - 100.0)");
            sb.AppendLine("      iStatusWord : Word;         // Drive status word (STW1 from drive)");
            sb.AppendLine("      iActualSpeed : Int;          // Actual speed from drive (0-16384 = 0-100%)");
            sb.AppendLine("      iCfg_MinSpeed : Real := 0.0;");
            sb.AppendLine("      iCfg_MaxSpeed : Real := 100.0;");
            sb.AppendLine("      iCfg_RampTime : Time := T#5S;");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("   VAR_OUTPUT");
            sb.AppendLine("      oControlWord : Word;        // Control word to drive (STW1)");
            sb.AppendLine("      oSpeedRef : Int;             // Speed reference to drive (0-16384)");
            sb.AppendLine("      oIsRunning : Bool;");
            sb.AppendLine("      oIsReady : Bool;");
            sb.AppendLine("      oFault : Bool;");
            sb.AppendLine("      oFaultCode : Int;");
            sb.AppendLine("      oActualSpeedPct : Real;     // Actual speed in %");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("   VAR");
            sb.AppendLine("      _state : DInt;");
            sb.AppendLine("      _ctrlWord : Word;");
            sb.AppendLine("      _StartCmdPrev : Bool;");
            sb.AppendLine("      _ResetCmdPrev : Bool;");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("   VAR_TEMP");
            sb.AppendLine("      _t_speed_limited : Real;");
            sb.AppendLine("      _t_stw_ready : Bool;");
            sb.AppendLine("      _t_stw_running : Bool;");
            sb.AppendLine("      _t_stw_fault : Bool;");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("   VAR CONSTANT");
            sb.AppendLine("      _STATE_OFF : DInt := 0;");
            sb.AppendLine("      _STATE_READY : DInt := 1;");
            sb.AppendLine("      _STATE_RUNNING : DInt := 2;");
            sb.AppendLine("      _STATE_FAULT : DInt := 10;");
            sb.AppendLine("      // STW1 bit masks for SINAMICS Standard Telegram 1");
            sb.AppendLine("      _STW_ON : Word := 16#0001;          // Bit 0: ON/OFF1");
            sb.AppendLine("      _STW_NO_COAST : Word := 16#0002;    // Bit 1: No coast stop");
            sb.AppendLine("      _STW_NO_QUICK : Word := 16#0004;    // Bit 2: No quick stop");
            sb.AppendLine("      _STW_ENABLE_OP : Word := 16#0008;   // Bit 3: Enable operation");
            sb.AppendLine("      _STW_ENABLE_RMP : Word := 16#0010;  // Bit 4: Enable ramp gen");
            sb.AppendLine("      _STW_UNFREEZE_RMP : Word := 16#0020;// Bit 5: Unfreeze ramp");
            sb.AppendLine("      _STW_ENABLE_SP : Word := 16#0040;   // Bit 6: Enable setpoint");
            sb.AppendLine("      _STW_FAULT_ACK : Word := 16#0080;   // Bit 7: Fault acknowledge");
            sb.AppendLine("      _ZSW_READY : Word := 16#0001;       // Bit 0: Ready to switch on");
            sb.AppendLine("      _ZSW_RUNNING : Word := 16#0002;     // Bit 1: Ready to operate");
            sb.AppendLine("      _ZSW_FAULT : Word := 16#0008;       // Bit 3: Fault active");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("BEGIN");
            sb.AppendLine();
            sb.AppendLine("REGION Parse status word");
