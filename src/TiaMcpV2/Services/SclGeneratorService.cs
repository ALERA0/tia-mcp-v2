            sb.AppendLine("VERSION : 1.0");
            sb.AppendLine("//PID controller with 3-step output for motorized valves (open/close pulses)");
            sb.AppendLine();
            sb.AppendLine("   VAR_INPUT");
            sb.AppendLine("      iSetpoint : Real;");
            sb.AppendLine("      iProcessValue : Real;");
            sb.AppendLine("      iPositionFeedback : Real;   // Valve position feedback 0-100%");
            sb.AppendLine("      iAutoMode : Bool := TRUE;");
            sb.AppendLine("      iManualOpen : Bool;");
            sb.AppendLine("      iManualClose : Bool;");
            sb.AppendLine("      iEnable : Bool := TRUE;");
            sb.AppendLine("      iCfg_Kp : Real := 1.0;");
            sb.AppendLine("      iCfg_Ti : Real := 60.0;");
            sb.AppendLine("      iCfg_Td : Real := 0.0;");
            sb.AppendLine("      iCfg_Deadband : Real := 2.0; // Output deadband %");
            sb.AppendLine("      iCfg_MinPulse : Time := T#500MS; // Minimum pulse time");
            sb.AppendLine("      iCfg_TravelTime : Time := T#60S;  // Full travel time");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("   VAR_OUTPUT");
            sb.AppendLine("      oOpenCmd : Bool;             // Open pulse output");
            sb.AppendLine("      oCloseCmd : Bool;            // Close pulse output");
            sb.AppendLine("      oOutputPosition : Real;      // Calculated position 0-100%");
            sb.AppendLine("      oError : Real;");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("   VAR");
            sb.AppendLine("      _integral : Real;");
            sb.AppendLine("      _prevError : Real;");
            sb.AppendLine("      _output : Real;");
            sb.AppendLine("      _positionCalc : Real;");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("   VAR CONSTANT");
            sb.AppendLine("      _CYCLE_TIME : Real := 0.1;");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("BEGIN");
            sb.AppendLine();
            sb.AppendLine("REGION PID + 3-Step output");
            sb.AppendLine("    IF NOT iEnable THEN");
            sb.AppendLine("        oOpenCmd := FALSE; oCloseCmd := FALSE;");
            sb.AppendLine("        _integral := 0.0;");
            sb.AppendLine("    ELSIF NOT iAutoMode THEN");
            sb.AppendLine("        oOpenCmd := iManualOpen;");
            sb.AppendLine("        oCloseCmd := iManualClose;");
            sb.AppendLine("    ELSE");
            sb.AppendLine("        // PID calculation");
            sb.AppendLine("        oError := iSetpoint - iProcessValue;");
            sb.AppendLine("        IF iCfg_Ti > 0.0 THEN");
            sb.AppendLine("            _integral := _integral + (oError * _CYCLE_TIME / iCfg_Ti);");
            sb.AppendLine("            IF _integral > 100.0 THEN _integral := 100.0; END_IF;");
            sb.AppendLine("            IF _integral < -100.0 THEN _integral := -100.0; END_IF;");
            sb.AppendLine("        END_IF;");
            sb.AppendLine("        _output := iCfg_Kp * (oError + _integral);");
            sb.AppendLine("        IF _output > 100.0 THEN _output := 100.0; END_IF;");
            sb.AppendLine("        IF _output < 0.0 THEN _output := 0.0; END_IF;");
            sb.AppendLine("        oOutputPosition := _output;");
            sb.AppendLine();
            sb.AppendLine("        // 3-Step: compare desired vs actual position");
            sb.AppendLine("        IF (_output - iPositionFeedback) > iCfg_Deadband THEN");
            sb.AppendLine("            oOpenCmd := TRUE;");
            sb.AppendLine("            oCloseCmd := FALSE;");
            sb.AppendLine("        ELSIF (iPositionFeedback - _output) > iCfg_Deadband THEN");
            sb.AppendLine("            oOpenCmd := FALSE;");
            sb.AppendLine("            oCloseCmd := TRUE;");
            sb.AppendLine("        ELSE");
            sb.AppendLine("            oOpenCmd := FALSE;");
            sb.AppendLine("            oCloseCmd := FALSE;");
            sb.AppendLine("        END_IF;");
            sb.AppendLine("        _prevError := oError;");
            sb.AppendLine("    END_IF;");
            sb.AppendLine("END_REGION");
            sb.AppendLine();
            sb.AppendLine("END_FUNCTION_BLOCK");
            return sb.ToString();
        }

