            sb.AppendLine();
            sb.AppendLine("   VAR CONSTANT");
            sb.AppendLine("      _PHASE_IDLE : DInt := 0;");
            sb.AppendLine("      _PHASE_RUNNING : DInt := 1;");
            sb.AppendLine("      _PHASE_HELD : DInt := 2;");
            sb.AppendLine("      _PHASE_COMPLETE : DInt := 3;");
