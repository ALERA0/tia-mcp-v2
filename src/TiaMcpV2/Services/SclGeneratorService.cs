        public string GenerateScl(string templateName, string blockName, Dictionary<string, string>? parameters = null)
        {
            switch (templateName.ToLowerInvariant())
            {
                case "motordol": return GenerateMotorDOL(blockName, parameters);
                case "motorstardelta": return GenerateMotorStarDelta(blockName, parameters);
                case "motorvfd": return GenerateMotorVFD(blockName, parameters);
                case "valveonoff": return GenerateValveOnOff(blockName, parameters);
                case "valveanalog": return GenerateValveAnalog(blockName, parameters);
                case "analoginput": return GenerateAnalogInput(blockName, parameters);
                case "analogoutput": return GenerateAnalogOutput(blockName, parameters);
                case "pidcontrol": return GeneratePIDControl(blockName, parameters);
                case "statemachine": return GenerateStateMachine(blockName, parameters);
                case "alarmhandler": return GenerateAlarmHandler(blockName, parameters);
                case "udt_motordata": return GenerateUdtMotorData(blockName);
                case "udt_valvedata": return GenerateUdtValveData(blockName);
                case "udt_analogdata": return GenerateUdtAnalogData(blockName);
                case "sequence": return GenerateSequence(blockName, parameters);
                case "communication": return GenerateCommunication(blockName, parameters);
                case "pid3step": return GeneratePID3Step(blockName, parameters);
                case "valvethreeway": return GenerateValveThreeWay(blockName, parameters);
                case "udt_piddata": return GenerateUdtPidData(blockName);
                case "modbusclient": return GenerateModbusClient(blockName, parameters);
                case "energymeter": return GenerateEnergyMeter(blockName, parameters);
                case "commissioningchecklist": return GenerateCommissioningChecklist(blockName);
                default:
                    throw new PortalException(PortalErrorCode.InvalidArgument, $"Unknown template: {templateName}. Use GetSclTemplates to see available templates.");
            }
        }

        public void GenerateAndImport(string softwarePath, string groupPath, string templateName, string blockName, Dictionary<string, string>? parameters = null)
        {
            var sclCode = GenerateScl(templateName, blockName, parameters);
            _blockService.ImportSclSource(softwarePath, groupPath, sclCode, blockName);
            _logger?.LogInformation("Generated and imported {Template} as {Name}", templateName, blockName);
        }

