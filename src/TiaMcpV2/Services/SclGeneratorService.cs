            sb.AppendLine("      _t_dTerm : Real;");
            sb.AppendLine("      _t_dt : Real;");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("   VAR CONSTANT");
            sb.AppendLine("      _CYCLE_TIME : Real := 0.1;  // 100ms cycle time - adjust to actual OB");
            sb.AppendLine("   END_VAR");
            sb.AppendLine();
            sb.AppendLine("BEGIN");
            sb.AppendLine();
            sb.AppendLine("REGION PID calculation");
            sb.AppendLine("    IF NOT iEnable THEN");
            sb.AppendLine("        _output := 0.0;");
            sb.AppendLine("        _integral := 0.0;");
            sb.AppendLine("        _initialized := FALSE;");
            sb.AppendLine("    ELSIF NOT iAutoMode THEN");
            sb.AppendLine("        // Manual mode - track manual value, reset integral");
            sb.AppendLine("        _output := iManualValue;");
            sb.AppendLine("        _integral := iManualValue / iCfg_Kp; // Bumpless transfer preparation");
            sb.AppendLine("        _initialized := TRUE;");
            sb.AppendLine("    ELSE");
            sb.AppendLine("        // Auto mode - PID");
            sb.AppendLine("        _t_error := iSetpoint - iProcessValue;");
            sb.AppendLine("        _t_dt := _CYCLE_TIME;");
            sb.AppendLine();
            sb.AppendLine("        IF NOT _initialized THEN");
            sb.AppendLine("            _prevError := _t_error;");
            sb.AppendLine("            _initialized := TRUE;");
            sb.AppendLine("        END_IF;");
            sb.AppendLine();
            sb.AppendLine("        // P term");
            sb.AppendLine("        _t_pTerm := iCfg_Kp * _t_error;");
            sb.AppendLine();
            sb.AppendLine("        // I term with anti-windup");
            sb.AppendLine("        IF iCfg_Ti > 0.0 THEN");
            sb.AppendLine("            _integral := _integral + (_t_error * _t_dt / iCfg_Ti);");
            sb.AppendLine("            // Anti-windup: clamp integral");
            sb.AppendLine("            IF _integral > (iCfg_OutputMax / iCfg_Kp) THEN _integral := iCfg_OutputMax / iCfg_Kp; END_IF;");
            sb.AppendLine("            IF _integral < (iCfg_OutputMin / iCfg_Kp) THEN _integral := iCfg_OutputMin / iCfg_Kp; END_IF;");
            sb.AppendLine("        END_IF;");
            sb.AppendLine("        _t_iTerm := iCfg_Kp * _integral;");
            sb.AppendLine();
            sb.AppendLine("        // D term");
            sb.AppendLine("        IF iCfg_Td > 0.0 AND _t_dt > 0.0 THEN");
            sb.AppendLine("            _t_dTerm := iCfg_Kp * iCfg_Td * (_t_error - _prevError) / _t_dt;");
            sb.AppendLine("        ELSE");
            sb.AppendLine("            _t_dTerm := 0.0;");
            sb.AppendLine("        END_IF;");
            sb.AppendLine("        _prevError := _t_error;");
            sb.AppendLine();
            sb.AppendLine("        _output := _t_pTerm + _t_iTerm + _t_dTerm;");
            sb.AppendLine("    END_IF;");
            sb.AppendLine("END_REGION");
            sb.AppendLine();
            sb.AppendLine("REGION Output limiting");
            sb.AppendLine("    IF _output < iCfg_OutputMin THEN _output := iCfg_OutputMin; END_IF;");
            sb.AppendLine("    IF _output > iCfg_OutputMax THEN _output := iCfg_OutputMax; END_IF;");
            sb.AppendLine("    oOutput := _output;");
            sb.AppendLine("    oOutputRaw := REAL_TO_INT(_output * 276.48);");
            sb.AppendLine("    oError := iSetpoint - iProcessValue;");
            sb.AppendLine("    oIsAutoMode := iAutoMode;");
            sb.AppendLine("END_REGION");
            sb.AppendLine();
            sb.AppendLine("END_FUNCTION_BLOCK");
            return sb.ToString();
        }

